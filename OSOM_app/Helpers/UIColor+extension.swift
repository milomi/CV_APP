import Foundation
import UIKit

extension UIColor {
    class var mainGrey: UIColor {
        return UIColor(red: 233.0 / 255.0, green: 235.0 / 255.0, blue: 238.0 / 255.0, alpha: 1.0)
    }
    
    class var separatorGray: UIColor {
        return UIColor(red: 216.0 / 255.0, green: 216.0 / 255.0, blue: 216.0 / 255.0, alpha: 1.0)
    }
}
